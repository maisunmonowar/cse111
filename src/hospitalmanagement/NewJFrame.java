    private void patientSetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_patientSetBtnActionPerformed
        // TODO add your handling code here:
        patientArr[patientI] = new patient(patientNameBox.getText(),
                Integer.parseInt(patientAgeBox.getText()),
                patientDiseaseBox.getText(),
                Integer.parseInt(patientIDBox.getText()));
        try {
            for (int i = 0; i < doc.length; i++) {
                if (patientDiseaseBox.getText().equalsIgnoreCase(doc[i].expertise)) {
                    doc[i].newPatient(patientArr[patientI]);
                    patientArr[patientI].docIndex = i;
                    break;
                }
            }
        } catch (NullPointerException e) {
            for (int i = 0; i < doc.length; i++) {
                if (doc[i].expertise.equalsIgnoreCase("medicine")) {
                    doc[i].newPatient(patientArr[patientI]);

                }
            }
        }

        patientArr[patientI].due += 1000;

        patientI++;
        
    }//GEN-LAST:event_patientSetBtnActionPerformed

