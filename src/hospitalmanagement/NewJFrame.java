    public int _billPatient(int id) {
        int i = 0;
        try {

            while (i < patientArr.length) {

                if (patientArr[i].show_id() == id) {
                    patientArr[i].due += 600;
                    break;
                }
                i++;
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(frame, "Patient not found!");
            return -1;
        }
        return i;
    }
