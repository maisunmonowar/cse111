    private void buyMedsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buyMedsBtnActionPerformed
        // TODO add your handling code here:
        int i = 0;
        String name = medsBuyBox.getText();
        try {
            if (name.equals("")) {
                NumberFormatException n = new NumberFormatException();
                throw n;
            }
            while (i < 99) {
                if (meds[i].name.equalsIgnoreCase(name)) {
                    break;
                } else {
                    i++;
                }
            }
            meds[i].buymedicine(Integer.parseInt(buyMedsAmount.getText()));
        } catch (NumberFormatException n) {
            JOptionPane.showMessageDialog(null, "Enter the medicine name!!");
        } catch (NullPointerException e) {
            meds[i] = new medicine(Integer.parseInt(buyPriceBox.getText()), Integer.parseInt(buyMedsAmount.getText()), name, "");
            JOptionPane.showMessageDialog(null, "New Medicine bought");
        }

    }//GEN-LAST:event_buyMedsBtnActionPerformed

